namespace EnumSerializer.TestGenerator.Diagnostics;

[Generator(LanguageNames.CSharp)]
internal sealed class TypesGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(context =>
        {
            context.AddSource(DiagnosticTestAttributeName, DiagnosticTestAttributeSource);
        });
    }

    internal const string Namespace = "EnumSerializer.Test.Diagnostics";
    internal const string DiagnosticTestAttributeName = "DiagnosticTestAttribute";

    // lang=C#
    private const string DiagnosticTestAttributeSource = """
// <auto-generated/>
        
#nullable enable

namespace EnumSerializer.Test.Diagnostics;

[global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false)]
internal sealed class DiagnosticTestAttribute : global::System.Attribute
{
    internal DiagnosticTestAttribute() { }
}
""";
} // internal sealed class TypesGenerator
