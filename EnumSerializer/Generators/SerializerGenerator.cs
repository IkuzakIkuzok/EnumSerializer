using System.Collections.Generic;
using System.Collections.Immutable;
using static EnumSerializer.SymbolUtils;

namespace EnumSerializer.Generators;

[Generator(LanguageNames.CSharp)]
internal sealed class SerializerGenerator : IIncrementalGenerator
{
    private const int MaxStackAllocLength = 256;

    private const string AttributeFullName = $"{AttributesGenerator.Namespace}.{AttributesGenerator.EnumSerializableName}";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var sources = context.SyntaxProvider.ForAttributeWithMetadataName(
            AttributeFullName,
            static (node, token) => node is EnumDeclarationSyntax,
            static (context, token) => context
        ).Collect();
        context.RegisterSourceOutput(sources, Execute);
    } // public void Initialize (IncrementalGeneratorInitializationContext)

    private static void Execute(SourceProductionContext context, ImmutableArray<GeneratorAttributeSyntaxContext> sources)
    {
        if (sources.Length == 0) return;

        var usePooled = false;
        var builder = new StringBuilder();
        builder.Append(@"// <auto-generated/>

#nullable enable
");
        foreach (var type in sources)
        {
            try
            {
                var typeSymbol = (INamedTypeSymbol)type.TargetSymbol;

                var attrs = type.Attributes.Where(a => a.AttributeClass.GetFullName() == AttributeFullName);
                var args =
                    type.Attributes.Where(a => a.AttributeClass.GetFullName() == AttributeFullName)
                                   .Select(GetEnumSerializationInfo)
                                   .OfType<EnumSerializationInfo>()
                                   .Distinct(new EnumSerializationInfo.EqualityComparer());

                if (!args.Any()) continue;
                
                Generate(builder, typeSymbol, args, out var pooled);
                usePooled |= pooled;
            }
            catch
            {
                
            }
        }

        if (usePooled)
            builder.AppendLine(PooledBufferSource);

        context.AddSource("EnumSerializationExtensions.g.cs", builder.ToString());
    } // private static void Execute (SourceProductionContext, ImmutableArray<GeneratorAttributeSyntaxContext>)

    private static EnumSerializationInfo? GetEnumSerializationInfo(AttributeData attribute)
    {
        var args = attribute.ConstructorArguments;
        if (args.Length == 0) return default;
        if (args[0].Value is not INamedTypeSymbol enumType) return default;

        var caseSensitive = true;
        if (attribute.TryGetNamedArgumentValue("CaseSensitive", out bool cs))
            caseSensitive = cs;

        var methods = ExtensionMethods.All;
        if (attribute.TryGetNamedArgumentEnumValue<ExtensionMethods>("Methods", out var m))
            methods = m;

        return new(enumType, caseSensitive, methods);
    } // private static EnumSerializationInfo? GetEnumSerializationInfo (AttributeData)

    private static void Generate(StringBuilder builder, INamedTypeSymbol enumType, IEnumerable<EnumSerializationInfo> targetTypes, out bool usePooled)
    {
        usePooled = false;

        if (targetTypes.All(t => t.ExtensionMethods == ExtensionMethods.None))
            return;

        var enumShortName = enumType.Name;
        var enumName = enumType.GetFullyQualifiedName();

        var ns = enumType.ContainingNamespace.ToDisplayString();

        builder.Append($$"""

namespace {{ns}}
{
    /// <summary>
    /// Provides extension methods for serialization of the <see cref="{{enumName}}"/> enum.
    /// </summary>
    internal static class {{enumShortName}}SerializationExtensions
    {
""");

        var hasToString = targetTypes.Any(t => t.GenerateToString);
        if (hasToString)
            GenerateToString(builder, enumType, targetTypes);

        #region TryParse

        var hasTryParse = targetTypes.Any(t => t.GenerateTryParse);
        if (hasTryParse)
            GenerateTryParse(builder, enumType, targetTypes, out usePooled);

        #endregion TryParse

        builder.AppendLine("""
    }
}
""");
    } // private static void Generate (StringBuilder, INamedTypeSymbol, IEnumerable<INamedTypeSymbol>)

    #region ToString

    private static void GenerateToString(StringBuilder builder, INamedTypeSymbol enumType, IEnumerable<EnumSerializationInfo> targetTypes)
    {
        var enumName = enumType.GetFullyQualifiedName();

        builder.AppendLine($$"""

        /// <summary>
        /// Serializes the specified <see cref="{{enumName}}"/> value to a string using the specified serialization attribute.
        /// </summary>
        /// <typeparam name="TAttr">The serialization attribute type.</typeparam>
        /// <param name="value">The <see cref="{{enumName}}"/> value to serialize.</param>
        /// <returns>The serialized string representation of the enum value.</returns>
        internal static string ToString<TAttr>(this {{enumName}} value) where TAttr : global::EnumSerializer.SerializeValueAttribute
        {
""");

        foreach (var targetType in targetTypes)
        {
            if (!targetType.GenerateToString) continue;

            var target = targetType.EnumType;
            if (!CheckInheritance(target, "EnumSerializer.SerializeValueAttribute"))
                continue;

            var targetFullName = target.GetFullyQualifiedName();
            builder.AppendLine($$"""
            if (typeof(TAttr) == typeof({{targetFullName}}))
                return {{GetSpecialToStringMethodName(target)}}(value);

""");
        }

        builder.AppendLine($$"""
            // Fallback to default ToString() if no matching attribute type is found
            return value.ToString();
        } // internal static string ToString<TAttr>(this {{enumName}}) where TAttr : global::EnumSerializer.SerializeValueAttribute
""");

        foreach (var target in targetTypes)
        {
            if (!target.GenerateToString) continue;
            GenerateSpecialToString(builder, enumName, enumType, target.EnumType);
        }
    } // private static void GenerateToString (StringBuilder)

    private static void GenerateSpecialToString(StringBuilder builder, string enumName, INamedTypeSymbol enumType, INamedTypeSymbol target)
    {
        if (!CheckInheritance(target, "EnumSerializer.SerializeValueAttribute"))
            return;

        var targetName = target.GetFullName();
        var targetFullName = target.GetFullyQualifiedName();
        var methodName = GetSpecialToStringMethodName(target);

        builder.AppendLine($$"""

        /// <summary>
        /// Serializes the specified <see cref="{{enumName}}"/> value to a string using the <see cref="{{targetFullName}}"/> attribute.
        /// </summary>
        /// <param name="value">The <see cref="{{enumName}}"/> value to serialize.</param>
        /// <returns>The serialized string representation of the enum value.</returns>
        internal static string {{methodName}}(this {{enumName}} value)
        {
            return value switch
            {
""");

        var fields = enumType.GetMembers().OfType<IFieldSymbol>().Where(f => f.IsStatic);
        var cases = new Dictionary<string, string>();
        var length = 0;
        foreach (var field in fields)
        {
            var attr = field.GetAttributes().FirstOrDefault(a => a.AttributeClass.GetFullName() == targetName);
            if (attr is null) continue;
            var args = attr.ConstructorArguments;
            if (args.Length == 0) continue;
            var serializedValue = args[0].Value?.ToString() ?? string.Empty;
            var key = $"{enumName}.{field.Name}";
            cases.Add(key, serializedValue);
            if (key.Length > length)
                length = key.Length;
        }

        foreach ((var key, var value) in cases)
        {
            builder.Append($"\t\t\t\t{key}");
            builder.Append(' ', length - key.Length);
            var s_value = string.IsNullOrEmpty(value) ? "string.Empty" : $"\"{value}\"";
            builder.AppendLine($" => {s_value},");
        }

        builder.AppendLine("""
                _ => value.ToString(),
            };
        }
""");
    } // private static void GenerateSpecialToString (StringBuilder, string, INamedTypeSymbol, INamedTypeSymbol)

    private static string GetSpecialToStringMethodName(INamedTypeSymbol target)
    {
        const string Suffix = "Attribute";

        var name = target.Name;
        if (name.EndsWith(Suffix))
            name = name[..^Suffix.Length];
        return $"To{name}";
    } // private static string GetSpecialToStringMethodName (INamedTypeSymbol)

    #endregion ToString

    #region TryParse

    private static void GenerateTryParse(StringBuilder builder, INamedTypeSymbol enumType, IEnumerable<EnumSerializationInfo> targetTypes, out bool usePooled)
    {
        usePooled = false;

        var enumShortName = enumType.Name;
        var enumName = enumType.GetFullyQualifiedName();

        builder.AppendLine($$"""

        /// <summary>
        /// Deserializes the specified string to a <see cref="{{enumName}}"/> value using the specified serialization attribute.
        /// </summary>
        /// <typeparam name="TAttr">The serialization attribute type.</typeparam>
        /// <param name="text">The string representation of the enum value.</param>
        /// <returns>The deserialized <see cref="{{enumName}}"/> value.</returns>
        internal static bool TryParse{{enumShortName}}<TAttr>(this global::System.ReadOnlySpan<char> text, out {{enumName}} value) where TAttr : global::EnumSerializer.SerializeValueAttribute
        {
""");
        foreach (var targetType in targetTypes)
        {
            if (!targetType.GenerateTryParse) continue;

            var target = targetType.EnumType;
            if (!CheckInheritance(target, "EnumSerializer.SerializeValueAttribute"))
                return;

            var targetFullName = target.GetFullyQualifiedName();
            builder.AppendLine($$"""
            if (typeof(TAttr) == typeof({{targetFullName}}))
                return {{GetTryParseMethodName(target, enumShortName)}}(text, out value);

""");
        }

        builder.AppendLine("""
            // No matching attribute type found
            value = default;
            return false;
        }
""");

        foreach (var target in targetTypes)
        {
            if (!target.GenerateTryParse) continue;
            GenerateTryParseFromString(builder, enumName, enumType, target, out var buffer);
            usePooled |= buffer;
        }
    } // private static void GenerateTryParse (StringBuilder, INamedTypeSymbol, IEnumerable<EnumSerializationInfo>, out bool)

    private static void GenerateTryParseFromString(StringBuilder builder, string enumName, INamedTypeSymbol enumType, EnumSerializationInfo info, out bool useBuffer)
    {
        useBuffer = false;

        var target = info.EnumType;
        if (!CheckInheritance(target, "EnumSerializer.SerializeValueAttribute"))
            return;

        var targetFullName = target.GetFullyQualifiedName();
        var methodName = GetTryParseMethodName(target, enumType.Name);

        var cs = info.CaseSensitive;

        var fields = enumType.GetMembers().OfType<IFieldSymbol>().Where(f => f.IsStatic);
        var cases = new Dictionary<string, string>();
        foreach (var field in fields)
        {
            var attr = field.GetAttributes().FirstOrDefault(a => a.AttributeClass?.GetFullyQualifiedName() == targetFullName);
            if (attr is null) continue;
            var args = attr.ConstructorArguments;
            if (args.Length == 0) continue;
            var serializedValue = args[0].Value?.ToString() ?? string.Empty;
            if (cases.ContainsKey(serializedValue))
                continue;
            cases.Add(serializedValue, field.Name);
        }

        var comment = cs ? "Comparison is case-sensitive." : "Comparison is case-insensitive.";

        if (cases.Count == 0)
        {
            GenerateEmptyTryParse(builder, enumName, targetFullName, methodName);
            return;
        }

        if (cases.Count == 1)
        {
            var onlyCase = cases.First();
            GenerateOnlyOneTryParse(builder, onlyCase, enumName, targetFullName, methodName, cs);
            return;
        }

        var lengths = cases.Keys.Select(k => k.Length).Distinct().ToArray();
        var len_op = lengths.Length == 1 ? "!=" : ">";
        var length = lengths.Max();
        useBuffer = !cs && length > MaxStackAllocLength;

        builder.AppendLine($$"""

        /// <summary>
        /// Attempts to deserialize the specified string to a <see cref="{{enumName}}"/> value using the <see cref="{{targetFullName}}"/> attribute.
        /// {{comment}}
        /// </summary>
        /// <param name="text">The string representation of the enum value.</param>
        /// <param name="value">When this method returns, contains the deserialized <see cref="{{enumName}}"/> value if the parsing succeeded, or the default value if the parsing failed.</param>
        /// <returns><see langword="true"/> if the parsing succeeded; otherwise, <see langword="false"/>.</returns>
        internal static bool {{methodName}}(this global::System.ReadOnlySpan<char> text, out {{enumName}} value)
        {
            if (text.Length {{len_op}} {{length}})
            {
                value = default;
                return false;
            }

""");
        if (cs)
        {
            // Case-sensitive, so we can directly switch on the input text without any transformation
            builder.AppendLine($$"""
            switch (text)
            {
""");
        }
        else if (length > MaxStackAllocLength)
        {
            // Case-insensitive and the length of the longest case exceeds the threshold for stack allocation
            builder.AppendLine($$"""
            // `text.Length` may exceed {{MaxStackAllocLength}} here, so we use pooled buffer to avoid stack overflow in case of very long input
            using var pooled = new global::EnumSerializer.PooledBuffer(text.Length);
            var lowerBuffer = text.Length <= {{MaxStackAllocLength}} ? stackalloc char[text.Length] : pooled.GetSpan();
            global::System.MemoryExtensions.ToLowerInvariant(text, lowerBuffer);

            switch (lowerBuffer)
            {
""");
        }
        else
        {
            // Case-insensitive and the length of the longest case does not exceed the threshold for stack allocation
            builder.AppendLine($$"""
            // `text.Length` never exceeds {{length}} here, so we can safely use stack allocation for the lowercase buffer
            var lowerBuffer = (stackalloc char[text.Length]);
            global::System.MemoryExtensions.ToLowerInvariant(text, lowerBuffer);

            switch (lowerBuffer)
            {
""");
        }

        foreach ((var key, var value) in cases)
        {
            var k = cs ? key : key.ToLowerInvariant();
            builder.AppendLine($"                case \"{k}\":");;
            builder.AppendLine($"                    value = {enumName}.{value};");
            builder.AppendLine($"                    return true;");
        }

        builder.AppendLine("""
            }

            value = default;
            return false;
        }
""");
    } // private static void GenerateTryParseFromString (StringBuilder, string, INamedTypeSymbol, EnumSerializationInfo, out bool)

    private static void GenerateEmptyTryParse(StringBuilder builder, string enumName, string targetFullName, string methodName)
    {
        builder.AppendLine($$"""

        /// <summary>
        /// Attempts to deserialize the specified string to a <see cref="{{enumName}}"/> value using the <see cref="{{targetFullName}}"/> attribute.
        /// </summary>
        /// <param name="text">The string representation of the enum value.</param>
        /// <param name="value">The default value of {{enumName}}.</param>
        /// <returns><see langword="false"/>.</returns>
        /// <remarks>Since there are no enum members decorated with the <see cref="{{targetFullName}}"/> attribute,
        /// this method always returns <see langword="false"/>.</remarks>
        internal static bool {{methodName}}(this global::System.ReadOnlySpan<char> text, out {{enumName}} value)
        {
            value = default;
            return false;
        }
""");
    } // private static void GenerateEmptyTryParse (StringBuilder, string, string, string)

    private static void GenerateOnlyOneTryParse(StringBuilder builder, KeyValuePair<string, string> onlyCase, string enumName, string targetFullName, string methodName, bool cs)
    {
        
        var option = cs ? "Ordinal" : "OrdinalIgnoreCase";
        var comment = cs ? "Comparison is case-sensitive." : "Comparison is case-insensitive.";

        builder.AppendLine($$"""

        /// <summary>
        /// Attempts to deserialize the specified string to a <see cref="{{enumName}}"/> value using the <see cref="{{targetFullName}}"/> attribute.
        /// {{comment}}
        /// </summary>
        /// <param name="text">The string representation of the enum value.</param>
        /// <param name="value">When this method returns, contains the deserialized <see cref="{{enumName}}"/> value if the parsing succeeded, or the default value if the parsing failed.</param>
        /// <returns><see langword="true"/> if the parsing succeeded; otherwise, <see langword="false"/>.</returns>
        internal static bool {{methodName}}(this global::System.ReadOnlySpan<char> text, out {{enumName}} value)
        {
            if (global::System.MemoryExtensions.Equals(text, "{{onlyCase.Key}}", global::System.StringComparison.{{option}}))
            {
                value = {{enumName}}.{{onlyCase.Value}};
                return true;
            }

            value = default;
            return false;
        }
""");
    } // private static void GenerateOnlyOneTryParse (StringBuilder, KeyValuePair<string, string>, string, string, string, bool)

    private static string GetTryParseMethodName(INamedTypeSymbol target, string enumName)
    {
        var name = target.Name;
        if (name.EndsWith("Attribute"))
            name = name[..^"Attribute".Length];
        return $"TryParse{enumName}From{name}";
    } // private static string GetTryParseMethodName (INamedTypeSymbol, string)

    #endregion TryParse

    #region buffer

    // lang=C#
    private const string PooledBufferSource = """

namespace EnumSerializer
{
    /// <summary>
    /// Provides a buffer of a specified length that is rented from a shared array pool and released when disposed.
    /// </summary>
    file ref struct PooledBuffer
    {
        private readonly int _length;
        private char[]? _buffer = null;

        /// <summary>
        /// Initializes a new instance of the <see cref="global::EnumSerializer.PooledBuffer"/> struct with the specified length.
        /// </summary>
        /// <param name="length">The size of the buffer, in bytes.</param>
        /// <remarks>
        /// The buffer is not allocated (or rented) at this point.
        /// It is allocated (or rented) when <see cref="GetSpan"/> is called for the first time.
        /// </remarks>
        internal PooledBuffer(int length)
        {
            global::System.ArgumentOutOfRangeException.ThrowIfNegativeOrZero(length, nameof(length));
            this._length = length;
        }

        /// <summary>
        /// Returns a span representing the valid portion of the underlying buffer.
        /// </summary>
        /// <returns>A <see cref="global::System.Span{char}"/> containing the elements in the buffer up to the current length.</returns>
        [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
        internal global::System.Span<char> GetSpan()
        {
            this._buffer ??= global::System.Buffers.ArrayPool<char>.Shared.Rent(this._length);
            return global::System.MemoryExtensions.AsSpan(this._buffer, 0, this._length);
        }

        /// <summary>
        /// Releases resources used by the instance and returns the underlying buffer to the shared array pool.
        /// </summary>
        public void Dispose()
        {
            var buffer = this._buffer;
            if (buffer is null) return;
            this._buffer = null;
            global::System.Buffers.ArrayPool<char>.Shared.Return(buffer);
        }
    }
}
""";

    #endregion buffer
} // internal sealed class SerializerGenerator : IIncrementalGenerator
